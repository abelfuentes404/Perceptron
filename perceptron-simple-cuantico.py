def ejecutar_modo_clasico(perceptron):
    while True:
        print("\n--- üîç Predicci√≥n de Pago (Cl√°sico) ---")
        try:
            monto = float(input("Monto del pr√©stamo: "))
            ingreso = float(input("Ingreso mensual del usuario: "))
        except ValueError:
            print("‚ùå Por favor, ingresa n√∫meros v√°lidos.")
            continue

        # Normalizar y predecir
        X_nuevo = np.array([monto, ingreso])
        X_nuevo_norm = perceptron.normalizar_dato(X_nuevo)
        prediccion = perceptron.predecir(X_nuevo_norm)
        
        resultado = "‚úÖ PAGAR√Å" if prediccion == 1 else "‚ùå NO PAGAR√Å"
        print(f"üéØ Predicci√≥n: {resultado}")

        # Feedback y aprendizaje online
        corroboracion = input("¬øLa predicci√≥n fue correcta? (s√≠/no): ")
        
        if es_respuesta_negativa(corroboracion):
            try:
                verdadera_etiqueta = int(input("Ingresa la etiqueta correcta (1 para PAG√ì, 0 para NO PAG√ì): "))
                
                # Ajustar pesos con aprendizaje online
                perceptron.aprender_online(X_nuevo, verdadera_etiqueta)
                print("üîÑ Modelo actualizado con tu feedback.")
                perceptron.guardar_modelo()
            except ValueError:
                print("‚ùå Etiqueta no v√°lida. No se actualiz√≥ el modelo.")
        else:
            print("‚úÖ Predicci√≥n correcta. Refuerzo positivo aplicado.")
            perceptron.aprender_online(X_nuevo, prediccion)
            perceptron.guardar_modelo()

        # Preguntar si desea continuar
        continuar = input("¬øPredecir otro? (s√≠/no): ")
        if es_respuesta_negativa(continuar):
            break


